	public function send() {
		if ($this->request->server['REQUEST_METHOD'] == 'POST') {
			$mail = new Mail();
			$mail->protocol = $this->config->get('config_mail_protocol');
			$mail->parameter = $this->config->get('config_mail_parameter');
			$mail->smtp_hostname = $this->config->get('config_mail_smtp_hostname');
			$mail->smtp_username = $this->config->get('config_mail_smtp_username');
			$mail->smtp_password = html_entity_decode($this->config->get('config_mail_smtp_password'), ENT_QUOTES, 'UTF-8');
			$mail->smtp_port = $this->config->get('config_mail_smtp_port');
			$mail->smtp_timeout = $this->config->get('config_mail_smtp_timeout');

			$mail->setTo($this->config->get('config_email'));
			$mail->setFrom($this->request->post['email']);
			$mail->setSender(html_entity_decode($this->request->post['name'], ENT_QUOTES, 'UTF-8'));
			$mail->setSubject(html_entity_decode(sprintf($this->language->get('email_subject'), $this->request->post['name']), ENT_QUOTES, 'UTF-8'));

			$message = 'Produkt: ' . $this->request->post['product_name'] . "\n";
			$message .= 'Objednávací kód: ' . $this->request->post['order_code'] . "\n";
			$message .= $this->request->post['message'];

			$mail->setText(html_entity_decode($message, ENT_QUOTES, 'UTF-8'));
			$mail->send();

			$this->response->redirect($this->url->link('information/contact/success'));
		}
	}

	public function printCategory() {
		$data = array();

		$data += $this->load->language('product/print_category');

		if ($this->request->server['HTTPS']) {
			$data['base'] = HTTPS_SERVER;
		} else {
			$data['base'] = HTTP_SERVER;
		}

		$this->load->model('catalog/category');

		$this->load->model('catalog/product');

		if (isset($this->request->get['path'])) {
			$url = '';

			if (isset($this->request->get['sort'])) {
				$url .= '&sort=' . $this->request->get['sort'];
			}

			if (isset($this->request->get['order'])) {
				$url .= '&order=' . $this->request->get['order'];
			}

			if (isset($this->request->get['limit'])) {
				$url .= '&limit=' . $this->request->get['limit'];
			}

			$path = '';

			$parts = explode('_', (string)$this->request->get['path']);

			$category_id = (int)array_pop($parts);
		} else {
			$category_id = 0;
		}

		$filter_data = array(
			'filter_category_id' => $category_id
		);	

		$results = $this->model_catalog_product->getProducts($filter_data);

		$data['products'] = array();

		foreach($results as $result) {
			$data['products'][] = array(
				'breakup_id' 	=> $result['breakup_id'],
				'order_code' 	=> $result['order_code'],
				'name' 			=> $result['name'],
				'price' 		=> $this->currency->format($result['price'])
			);
		}

		usort($data['products'], function($a, $b) {
		    return $a['breakup_id'] - $b['breakup_id'];
		});

		$category_info = $this->model_catalog_category->getCategory($category_id);

		$data['heading_title'] = $category_info['name'];
		
		$data['breakup_image'] = $category_info['breakup_image'];

		$this->response->setOutput($this->load->view('default/template/product/print_category.tpl', $data));
